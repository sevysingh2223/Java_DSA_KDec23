public class Series6 {
    public static void main(String[] args) {
        
        int n =5;
        float sum =0;
        for(float i=1;i<=n;i++){
            if(i%2!=0){
                // System.out.println(i/(i+1));
                sum = sum + (i/(i+1));
            }
            else{
                // System.out.println(i/(i+1));
                sum -= (i/(i+i));
            }
        }
        System.out.println("SUm of series is : "+ sum);
    }
}



